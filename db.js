// db.js
require("dotenv").config();

const { Pool } = require("pg");

const databaseUrl = process.env.DATABASE_URL;

if (!databaseUrl) {
  console.warn(
    "Falta DATABASE_URL. Define DATABASE_URL en .env local o en las variables de Heroku.",
  );
}

function shouldUseSsl(connectionString = "") {
  if (!connectionString) return false;

  const normalized = connectionString.toLowerCase();

  // En local normalmente NO usamos SSL.
  if (
    normalized.includes("localhost") ||
    normalized.includes("127.0.0.1") ||
    normalized.includes("::1")
  ) {
    return false;
  }

  // Heroku Postgres y la mayoría de bases remotas exigen SSL.
  return true;
}

const pool = new Pool({
  connectionString: databaseUrl,
  ssl: shouldUseSsl(databaseUrl)
    ? {
        rejectUnauthorized: false,
      }
    : false,
});

async function query(text, params) {
  return pool.query(text, params);
}

async function initDb() {
  await query(`
    CREATE TABLE IF NOT EXISTS chat_messages (
      id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      room TEXT NOT NULL DEFAULT 'general',
      username TEXT NOT NULL DEFAULT 'anonymous',
      text TEXT NOT NULL DEFAULT '',
      created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
    );
  `);

  await query(`
    CREATE INDEX IF NOT EXISTS idx_chat_messages_room_created_at
    ON chat_messages (room, created_at DESC);
  `);

  console.log("Database initialized");
}

module.exports = {
  pool,
  query,
  initDb,
};
